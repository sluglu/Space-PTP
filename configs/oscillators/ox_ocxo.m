% High-Performance OCXO (100MHz OX-249)
function ox = ox_ocxo()
    f0 = 100e6;
    np = NoiseProfile(struct(...
        'delta_f0', (rand() * 2 * 50) - 50, ...
        'alpha', (rand() * 2 * 1.58e-6) - 1.58e-6, ...
        'power_law_coeffs', [0, 4.62e-23, 1.58e-25, 0, 1.0e-32], ...
        'timestamp_resolution', 0));
    ox = struct( ...
         'f0', f0, ...
         'np', np);
end